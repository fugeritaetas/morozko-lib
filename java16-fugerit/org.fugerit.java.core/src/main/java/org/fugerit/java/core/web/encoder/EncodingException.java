package org.fugerit.java.core.web.encoder;

public class EncodingException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 3137488353756832630L;

	public EncodingException() {
		super();
	}

	public EncodingException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public EncodingException(String arg0) {
		super(arg0);
	}

	public EncodingException(Throwable arg0) {
		super(arg0);
	}

}
