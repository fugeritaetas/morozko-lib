package org.morozko.java.mod.parser.model;

public class ParserException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 6880891845026312877L;

	public ParserException() {
		super();
	}

	public ParserException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ParserException(String arg0) {
		super(arg0);
	}

	public ParserException(Throwable arg0) {
		super(arg0);
	}

	
}
