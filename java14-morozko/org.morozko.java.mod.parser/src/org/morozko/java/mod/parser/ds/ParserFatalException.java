package org.morozko.java.mod.parser.ds;

public class ParserFatalException extends Exception {

	public ParserFatalException() {
		super();
	}

	public ParserFatalException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ParserFatalException(String arg0) {
		super(arg0);
	}

	public ParserFatalException(Throwable arg0) {
		super(arg0);
	}

	/**
	 * 
	 */
	private static final long serialVersionUID = 6322816786185032942L;

}
