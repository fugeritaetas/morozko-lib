DROP TABLE PROPS_VALUES;
DROP TABLE PROPS_CATALOG;

DROP SEQUENCE seq_id_props;

CREATE SEQUENCE seq_id_props START WITH 1 INCREMENT BY 1000000 CACHE 100;

CREATE TABLE PROPS_CATALOG
(
  ID_PROPS_CATALOG   NUMBER                     NOT NULL,
  PROP_CAT           VARCHAR2(32) NOT NULL,
  PROP_DESCRIPTION   VARCHAR2(256)          NOT NULL
);
ALTER TABLE PROPS_CATALOG ADD CONSTRAINT PROPS_CATALOG_PK PRIMARY KEY ( ID_PROPS_CATALOG );


CREATE TABLE PROPS_VALUES
(
  ID_PROPS_VALUE     NUMBER                     NOT NULL,
  PROP_KEY           VARCHAR2(128)          NOT NULL,
  PROP_VALUE         VARCHAR2(4000)        ,
  ID_PROPS_CATALOG   NUMBER                     NOT NULL
);
ALTER TABLE PROPS_VALUES ADD CONSTRAINT PROPS_VALUES_PK PRIMARY KEY ( ID_PROPS_VALUE );
ALTER TABLE PROPS_VALUES ADD CONSTRAINT PROPS_VALUES_SK1 UNIQUE ( ID_PROPS_CATALOG, PROP_KEY );
ALTER TABLE PROPS_VALUES ADD CONSTRAINT PROPS_VALUES_FK1 FOREIGN KEY ( ID_PROPS_CATALOG ) 
	  REFERENCES PROPS_CATALOG ( ID_PROPS_CATALOG ) ON DELETE CASCADE;
