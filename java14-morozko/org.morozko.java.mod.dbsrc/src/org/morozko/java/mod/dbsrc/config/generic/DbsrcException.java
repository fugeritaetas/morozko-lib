package org.morozko.java.mod.dbsrc.config.generic;

public class DbsrcException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 4258883898862711045L;

	public DbsrcException() {
	}

	public DbsrcException(String message) {
		super(message);
	}

	public DbsrcException(Throwable cause) {
		super(cause);
	}

	public DbsrcException(String message, Throwable cause) {
		super(message, cause);
	}

}
